export const MARCAS_SORTED = Object.keys(MARCA_MODELOS).sort((a, b) =>
  a.localeCompare(b, 'pt-BR'),
)

function normalizeStr(s: string) {
  return s
    .trim()
    .toLowerCase()
    .normalize('NFD')
    .replace(/\p{M}/gu, '')
}

/** Encontra a chave exata do catálogo (acentos ignorados). */
export function findMarcaKey(typed: string): string | null {
  const t = normalizeStr(typed)
  if (!t) return null
  for (const m of MARCAS_SORTED) {
    if (normalizeStr(m) === t) return m
  }
  return null
}

export function filterMarcas(query: string, limit = 200): string[] {
  const q = normalizeStr(query)
  if (!q) return MARCAS_SORTED
  return MARCAS_SORTED.filter((m) => normalizeStr(m).includes(q)).slice(
    0,
    limit,
  )
}

export function filterModelos(
  marcaKey: string,
  query: string,
  limit = 200,
): string[] {
  const list = MARCA_MODELOS[marcaKey]
  if (!list?.length) return []
  const q = normalizeStr(query)
  if (!q) return list.slice(0, limit)
  return list.filter((mo) => normalizeStr(mo).includes(q)).slice(0, limit)
}
